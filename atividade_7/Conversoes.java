public class Conversoes {
    
    public static double gramasParaKg( double gramas ){
       return gramas/1000;
    }

    public static double kgParaGramas( double quilogramas ){
       return quilogramas*1000;
    }

    public static double poundParaKg( double pound ){
       return pound/2.2046;
    }

    public static double kgParaPound( double quilogramas ){
       return quilogramas*2.2046;
    }

    public static double ounceParaG( double ounce ){
       return ounce* 28.3495;
    }

    public static double gramasParaOunce( double gramas ){
       return gramas/28.3495;
    }

   

}